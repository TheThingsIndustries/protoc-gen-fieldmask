package main

import (
	"github.com/TheThingsIndustries/protoc-gen-fieldmask/module"
	"github.com/TheThingsIndustries/protoc-gen-fieldmask/processor"
	pgs "github.com/lyft/protoc-gen-star"
	pgsgo "github.com/lyft/protoc-gen-star/lang/go"
	pgvmodule "github.com/lyft/protoc-gen-validate/module"
)

const FileHeader = `// Code generated by protoc-gen-fieldmask. DO NOT EDIT.`

var initGoContext = pgsgo.InitGoGoContext

func main() {
	pgs.Init(
		pgs.DebugEnv("DEBUG"),
	).RegisterModule(
		module.Pather(initGoContext),
		module.Setter(initGoContext),
		pgvmodule.Validator(),
	).RegisterPostProcessor(
		processor.HeaderPrepender(FileHeader),
		pgsgo.GoFmt(),
	).Render()
}
